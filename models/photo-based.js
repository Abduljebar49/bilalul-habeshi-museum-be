class PhotoBased{
    constructor(
        id,
        name,
        description,
        category,
        photo,
        code,
        count,
        type
        ,
        audio
    ){
        this.id = id;
        this.name = name;
        this.description = description;
        this.category = category;
        this.photo = photo;
        this.code = code;
        this.count = count;
        this.type = type;
        this.audio = audio;
    }
}

module.exports = PhotoBased